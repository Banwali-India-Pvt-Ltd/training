// The program converts value to lower case
// class to create object
public class ToLowerCase 
{
	// main method to create program
	public static void main(String[] args)
	{
		String str1 = "Banwali";
		String str2 = "BANWALI";
		String str3 = "BAnWaLi";
		String str4 = "banwali123";
		
		System.out.println(str1.toLowerCase());  // banwali
		System.out.println(str2.toLowerCase());  // banwali
		System.out.println(str3.toLowerCase());  // banwali
		System.out.println(str4.toLowerCase());  // banwali123
	}

}
