// The program converts values to upper case
// class to create method
public class ToLowerCase 
{
	// main method to create program
	public static void main(String[] args)
	{
		System.out.println(Character.toLowerCase('a'));  // returns a
		System.out.println(Character.toLowerCase('A'));  // returns a
		System.out.println(Character.toLowerCase('S'));  // returns s
	}

}
