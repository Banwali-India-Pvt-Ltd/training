// The program returns the lower values from the string
// class to create method
public class StringToLowercase
{
	// main method to create program
	public static void main(String[] args)
	{
		String str1 = " banwali ";
		String str2 = " BANwaLi";
		String str3 = " BANWALI ";
		
		System.out.println(str1.toLowerCase());    // returns banwali
		System.out.println(str2.toLowerCase());    // returns banwali
		System.out.println(str3.toLowerCase());    // returns banwali
	}

}
