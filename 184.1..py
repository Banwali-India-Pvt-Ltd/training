# Demonstrates that lists may be heterogeneous; that is, a list can hold elements of varying types.
collection = [24.2, 4, 'word', eval, 19, -0.03, 'end']
print(collection[0])     # Print the first elements
print(collection[1])     # Print the second elements
print(collection[2])     # Print the third elements
print(collection[3])     # Print the fourth elements
print(collection[4])     # Print the fifth elements
print(collection[5])     # Print the sixth elements
print(collection[6])     # Print the seventh elements
print(collection)        # Print a collection variable
