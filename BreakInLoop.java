/* The program of break statement
 * break the number after defining a number
 */
// class name with first letter of capital
public class BreakInLoop {
	// used main method to write the program
	public static void main(String[] args){
		// implement for loop to print numbers
		for(int i =1; i<=10; i++)
		{
			// condition to break the loop
			if(i==5)
			{
				break;
				
			}
			System.out.println(i);
		}
}

}
