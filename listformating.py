# default arguments
print("Hello {}, your balance is {}.".format("sukhpal", 230.2346))

# positional aerguments
print("Hello {0}, your blcance is {1}.".format("sukhpal", 230.2346))

# keyword arguments
print("Hello{name}, your balance is {blc}.".format(name="sukhpal", blc=230.2346))

# mixed arguments
print("Hello {0}, your balance is {blc}.".format("sukhpal", blc=230.2346))