// The program checks weather the value is lower case or not
// class to create method
public class IsLowerCase 
{
	public static void main(String[] args)
	{
		System.out.println(Character.isLowerCase('C'));  // return false
		System.out.println(Character.isLowerCase('z'));  // returns true
		System.out.println(Character.isLowerCase('A'));  // returns false
	}

}
