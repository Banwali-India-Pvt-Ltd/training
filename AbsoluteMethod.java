// The program returns absolute value 
// class to create object
public class AbsoluteMethod
{
	// main method to create program
	public static void main(String[] args)
	{
		double a = 10.234;
		double b = 10.789;
		double c = -20.345;
		
		System.out.println(Math.abs(a));  // 10.234
		System.out.println(Math.abs(b));  // 10.789
		System.out.println(Math.abs(c));  // 20.345
	}

}
