# Tuples
tuple1 = (1,6,5,4,3)
print(tuple1)
print(len(tuple1))   # print tuple length
print(max(tuple1))   # print tuple max number
print(min(tuple1))   # print tuple min value

# Conver tuple into list
tuple1 = (1,5,4,7)    # create tuple
list1 = list(tuple1)   # convert tuple into list
print(list1)
del list1[2]
print(list1)



# length of list
print(len(list1))

# max of list
print(max(list1))

# min of list
print(min(list1))