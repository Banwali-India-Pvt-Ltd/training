// The program gives the Ranks 
// class to create object
public class VoidExample {
	// main method to create program
public static void main(String[] args){
	// define new rank 
	methodRank(255);
}
// define new method with name methodRank
public static void methodRank(int points){
	// implement the conditions to get the exact value
	if(points>=266){
		System.out.println("Rank1");
	}
	else if(points>=122){
		System.out.println("Rank2");
	}
	else{
		System.out.println("Rank3");
	}
	
}
}
