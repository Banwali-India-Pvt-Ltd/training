// The program checks weather the value is upper case or not
// class to create method
public class IsUpperCase 
{
	public static void main(String[] args)
	{
		System.out.println(Character.isUpperCase('C'));  // returns true
		System.out.println(Character.isUpperCase('z'));  // returns false
		System.out.println(Character.isUpperCase('a'));  // returns false
	}

}
