// The program is for print table via functions
// class to create object
public class TableInFuntions {
	// main method to create program
public static void main(String[] args){
	// define method to call it
	printTimesTable(2);
}
// define a new function
public static void printTimesTable(int param){
	// implement loop to print table 
	for(int i=1; i<=10; i++){
		// finally display table
		System.out.println(param * i);
	}
}
}
