// The program returns the nearest value in the round
// class to create object
public class RoundMethod 
{
	// main method to create program
	public static void main(String[] args)
	{
		double a = 10.234;
		double b = 10.789;
		double c = -20.345;
		
		System.out.println(Math.round(a));  // 10
		System.out.println(Math.round(b));  // 11
		System.out.println(Math.round(c));  // -20
	}
}
