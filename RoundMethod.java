// The program execute round numbers near the value

// class to create method
public class RoundMethod 
{
	// main method to create program
	public static void main(String[] args)
	{
		double a = 10.575;
		double b = 10.498;
		int c = 11;
		
		System.out.println(Math.round(a)); // 11
		System.out.println(Math.round(b)); // 10
		System.out.println(Math.round(c));  // 11
	}

}
