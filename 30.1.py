# The program is related to end= operator
from __future__ import print_function

print('A', end='')  # print A and continue with current line
print('B', end='')  # print B and continue with current line
print('C', end='')  # print C and continue with current line
print()    # print() to leave current line
print('x')     # print x and leave the current line
print('Y')     # print Y and leave the current line
print('Z')     # print Z and leave the current line