# The program is related to if/else statement

d1 = 1.11 - 1.10   # store value in d1 to calculate
d2 = 2.11 - 2.10   # store value in d2 to calculate
print('d1 =', d1, 'd2 =', d2)    # print d1 & d2 after calculating

if d1 == d2:     # if condition if d1 & d2 will be same then print same
    print('same')

else:            # else condition if not same then print different
    print('Defferent')