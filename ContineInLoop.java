/* The program is based on continue statement
 * The actual work of continue statement to get skip the given number
 */
// The class with name of first capital letter
public class ContineInLoop {
	// main method to use program
	public static void main(String[] args){
		// implement for loop to print numbers
	for(int i=1; i<=10; i++)
	{
		// implement if condition to skip three number
		if(i==3)
		{
			continue;
		}
		System.out.println(i);
	}

}
}
