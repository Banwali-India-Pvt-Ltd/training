# The program takes input from user and give sum of numbers
# In this program no need to give message print & give message to user

x = input('Please enter an integer value:') # message to directly enter value
y = input('Please enter another integer value:') # message to enter second value
num1 = int(x)  # store x value in num1
num2 = int(y)  # store y value in num2
print(num1, '+', num2, '=', num1 + num2)  # print total of num1 & num2