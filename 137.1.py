# Count to n and print each number on its own line
def count_to_n(n):
    for i in range(1, n + 1):
        print(i)
print("Going to count to ten . . .")
count_to_n(10);              # Call the function
print("Going to count to five . . .")
count_to_n(5);              # Call the function