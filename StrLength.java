// class to create object
public class StrLength 
{
	// main method to create program
	public static void main(String[] args)
	{
		String str1 = "Banwali Company";
		String str2 = "Testing";
		
		System.out.println(str1.length());   // 15
		System.out.println(str2.length());   // 7
	}

}
