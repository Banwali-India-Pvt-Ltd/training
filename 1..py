# About Functions

# This code Add and Subtract two define value

# a and b are parameters/arguments to the function add and Subtraction
def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

x = 10
z = 5

m = add(x, z)         # Call the add Function
print('Add:', m)      # Print m result

n = subtract(x, z)    # Call the subtract Function
print('Subtract:', n) # Print n result

