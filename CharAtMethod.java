// The program returns a character value by index
// class to create object
public class CharAtMethod 
{
	// main method to create program
	public static void main(String[] args)
	{
		String str1 = "Banwali";
		
		System.out.println(str1.charAt(1));  // a
		System.out.println(str1.charAt(6));  // i
	}

}
