# In this program we can use the + operator to add two integers


value1 = eval(input('Please enter a number:'))  # get input first value
value2 = eval(input('Please enter another number:'))  # get input second value
sum = value1 + value2 # store value1 & value2 in sum
print(value1, '+', value2, '=', sum)   # print sum of both numbers with hint