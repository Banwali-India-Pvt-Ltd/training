// The program returns part of the string based on index positions
// class to create object
public class SubStringMethod
{
	// main method to create program
	public static void main(String[] args)
	{
		String str1 = "Welcome to Banwali";
		
		System.out.println(str1.substring(11));
		System.out.println(str1.substring(7));
		System.out.println(str1.substring(8,10));
		System.out.println(str1.substring(2,5));
	}

}
