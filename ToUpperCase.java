// The program converts value to Upper case
// class to create method
public class ToUpperCase 
{
	// main method to write the program
	public static void main(String[] args)
	{
		System.out.println(Character.toUpperCase('a'));  // returns A
		System.out.println(Character.toUpperCase('B'));  // returns B
		System.out.println(Character.toUpperCase('A'));  // returns A
		System.out.println(Character.toUpperCase('c'));  // returns C
		
		
	}

}
