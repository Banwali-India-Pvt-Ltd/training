// The program converts values to upper case
// class to create object
public class ToUpperCase 
{
	// main method to create program
	public static void main(String[] args)
	{
		String str1 = "Banwali";
		String str2 = "BANWALI";
		String str3 = "BAnWaLi";
		String str4 = "banwali123";
		
		System.out.println(str1.toUpperCase());  // BANWALI
		System.out.println(str2.toUpperCase());  // BANWALI
		System.out.println(str3.toUpperCase());  // BANWALI
		System.out.println(str4.toUpperCase());  // BANWALI123
	}
}
